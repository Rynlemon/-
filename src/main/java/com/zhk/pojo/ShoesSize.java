package com.zhk.pojo;

public class ShoesSize {
    private Integer sizeId;
    private int sizeNumber;
    private int sizeStatus;
    private String sizeNotes;

    public Integer getSizeId() {
        return sizeId;
    }

    public void setSizeId(Integer sizeId) {
        this.sizeId = sizeId;
    }

    public int getSizeNumber() {
        return sizeNumber;
    }

    public void setSizeNumber(int sizeNumber) {
        this.sizeNumber = sizeNumber;
    }

    public int getSizeStatus() {
        return sizeStatus;
    }

    public void setSizeStatus(int sizeStatus) {
        this.sizeStatus = sizeStatus;
    }

    public String getSizeNotes() {
        return sizeNotes;
    }

    public void setSizeNotes(String sizeNotes) {
        this.sizeNotes = sizeNotes;
    }
}
